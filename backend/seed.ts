// Database bootstrap
// ---------------------------------------------------------------------------

const AppDataSource = new DataSource({
  type: 'sqlite',
  database: 'spellslot.sqlite',
  entities: [Spell],
  synchronize: false,
  logging: false,
});

async function seed() {
  console.log('Connecting to database…');
  await AppDataSource.initialize();

  const repo = AppDataSource.getRepository(Spell);

  let inserted = 0;
  let skipped = 0;

  for (const s of SPELLS) {
    const exists = await repo.findOneBy({ name: s.name });
    if (exists) {
      skipped++;
      continue;
    }

    const spell = repo.create({} as DeepPartial<Spell>);
    Object.assign(spell, {
      name: s.name,
      level: s.level,
      school: s.school,
      castTime: s.castTime,
      range: s.range,
      components: s.components,
      duration: s.duration,
      concentration: s.concentration,
      ritual: s.ritual,
      attackType: s.attackType,
      saveAbility: s.saveAbility ?? null,
      atkType: s.atkType ?? null,
      dmgDice: s.dmgDice ?? [],
      upcastDice: s.upcastDice ?? [],
      cantripDie: s.cantripDie ?? null,
      cantripType: s.cantripType ?? null,
      cantripAddMod: s.cantripAddMod ?? false,
      cantripScaling: s.cantripScaling ?? null,
      multiProj: s.multiProj ?? false,
      projBase: s.projBase ?? null,
      projUpcast: s.projUpcast ?? false,
      projUpcastEveryN: s.projUpcastEveryN ?? null,
      projUpcastAbove: s.projUpcastAbove ?? null,
      notes: s.notes ?? null,
    });

    await repo.save(spell);
    inserted++;
    process.stdout.write(
      `\r  Inserted: ${inserted} | Skipped (already exists): ${skipped}`,
    );
  }

  console.log(
    `\n\nDone! ${inserted} spells inserted, ${skipped} already existed.`,
  );
  await AppDataSource.destroy();
}

seed().catch((err) => {
  console.error('Seed failed:', err);
  process.exit(1);
});
